# Функция для поиска задачи по ID
def find_problem_by_id(problem_id):
    for problem in PROBLEMS_DB:
        if problem['id'] == problem_id:
            return problem
    return None

# Функция для фильтрации задач по параметрам
def filter_problems(subject=None, grade=None, difficulty=None):
    filtered = PROBLEMS_DB
    if subject:
        filtered = [p for p in filtered if p['subject'] == subject]
    if grade:
        filtered = [p for p in filtered if p['grade'] == grade]
    if difficulty:
        filtered = [p for p in filtered if p['difficulty'] == difficulty]
    return filtered

